# %%
x = 2
y = 10
# -------- CALCULADORA --------
# SOMA
print("x + y =", x + y)

# SUBITRAÇÃO
print("x - y =", x - y)

# MULTIPLICAÇÃO
print("x * y =", x * y)

# DIVISÃO
print("x / y =", x / y)

# RESTO DIVISÃO
print("x % y =", x % y, " obs.: sobra o resto da divisão")

# EXPONENCIAÇÃO
print("x ** y =", x ** y)

# DIVISÃO INTEIRA
print("x // y =", x // y, " obs.: divisão que pega só a parte inteira")